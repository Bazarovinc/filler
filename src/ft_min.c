int		ft_min(int *arr, int size)
{
	int i;
	int min;

	i = 0;
	min = 100;
	while (i < size)
	{
		if (arr[i] < min && arr[i] >= 1)
			min = arr[i];
		i++;
	}
	return (min);
}
